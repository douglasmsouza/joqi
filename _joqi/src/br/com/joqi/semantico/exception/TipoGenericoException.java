package br.com.joqi.semantico.exception;

public class TipoGenericoException extends Exception {

	public TipoGenericoException() {
		super();
	}

	public TipoGenericoException(String message, Throwable cause) {
		super(message, cause);
	}

	public TipoGenericoException(String message) {
		super(message);
	}

	public TipoGenericoException(Throwable cause) {
		super(cause);
	}

}
