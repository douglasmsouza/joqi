package br.com.joqi.semantico.exception;

public class TiposIncompativeisException extends Exception {

	public TiposIncompativeisException() {
		super();
	}

	public TiposIncompativeisException(String message, Throwable cause) {
		super(message, cause);
	}

	public TiposIncompativeisException(String message) {
		super(message);
	}

	public TiposIncompativeisException(Throwable cause) {
		super(cause);
	}

}
