package br.com.joqi.semantico.exception;

public class OperandosIncompativeisException extends Exception {

	public OperandosIncompativeisException() {
		super();
	}

	public OperandosIncompativeisException(String message, Throwable cause) {
		super(message, cause);
	}

	public OperandosIncompativeisException(String message) {
		super(message);
	}

	public OperandosIncompativeisException(Throwable cause) {
		super(cause);
	}

}
