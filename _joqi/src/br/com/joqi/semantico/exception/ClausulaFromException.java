package br.com.joqi.semantico.exception;

public class ClausulaFromException extends Exception {

	public ClausulaFromException() {
		super();
	}

	public ClausulaFromException(String message, Throwable cause) {
		super(message, cause);
	}

	public ClausulaFromException(String message) {
		super(message);
	}

	public ClausulaFromException(Throwable cause) {
		super(cause);
	}

}
