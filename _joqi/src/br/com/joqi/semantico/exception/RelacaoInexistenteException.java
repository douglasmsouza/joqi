package br.com.joqi.semantico.exception;

public class RelacaoInexistenteException extends Exception {

	public RelacaoInexistenteException() {
		super();
	}

	public RelacaoInexistenteException(String message, Throwable cause) {
		super(message, cause);
	}

	public RelacaoInexistenteException(String message) {
		super(message);
	}

	public RelacaoInexistenteException(Throwable cause) {
		super(cause);
	}

}
